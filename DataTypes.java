package revision;

public class DataTypes {
    public static void main(String[] args) {
        char u = 'A' ;
        System.out.println("Char " + u);
        float v = 10.5f ;
        System.out.println("Float "+ v);
        double w = 4.0 ;
        System.out.println("Double "+ w);
        long x = 9999999999L;
        System.out.println("Long " + x);
    }
}